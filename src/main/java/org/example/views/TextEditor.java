    private void openFileItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openFileItemActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("XML Files", "xml");
        chooser.setFileFilter(filter);
        int returnVal = chooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            System.out.println("You are selected file xml " + chooser.getSelectedFile().getName());
            try {
                File file = new File(chooser.getSelectedFile().getPath());
                FileReader fileReader = new FileReader(file);
                BufferedReader text = new BufferedReader(fileReader);
                StringBuilder content = new StringBuilder();
                String line;
                while ((line = text.readLine()) != null) {
                    content.append(line).append("\n");
                }
                textEditorArea.setText(content.toString());
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_openFileItemActionPerformed

